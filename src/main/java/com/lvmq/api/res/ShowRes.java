package com.lvmq.api.res;

public class ShowRes {

	private Double income;
	private String url;

	/**
	 * @return the income
	 */
	public Double getIncome() {
		return income;
	}

	/**
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}

	/**
	 * @param income
	 *            the income to set
	 */
	public void setIncome(Double income) {
		this.income = income;
	}

	/**
	 * @param url
	 *            the url to set
	 */
	public void setUrl(String url) {
		this.url = url;
	}

}
